// Convertir grados Celsius a Fahrenheit

// Este programa convierte una temperatura en grados Celsius
// a grados Fahrenheit utilizando una fórmula matemática.
// El resultado se muestra en la consola.

let celsius = 30;

let fahrenheit = (celsius * 9 / 5) + 32;

console.log("Celsius:", celsius);
console.log("Fahrenheit:", fahrenheit + "°F");

