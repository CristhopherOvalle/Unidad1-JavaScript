// Calcular edad según año actual y año de nacimiento

// Este programa calcula la edad de una persona
// restando el año de nacimiento al año actual.
// El resultado se muestra en la consola.

let anioActual = 2026;
let anioNacimiento = 2000;

let edad = anioActual - anioNacimiento;

console.log("Año actual:", anioActual);
console.log("Año de nacimiento:", anioNacimiento);
console.log("Edad:", edad + " años");